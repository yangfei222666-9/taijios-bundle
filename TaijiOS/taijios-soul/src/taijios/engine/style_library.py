# 风格选择
# ============================================================

def pick_style(message: str = "", use_combo: bool = False) -> dict:
    """
    根据消息内容 + 随机性，挑选1-2个风格。

    返回:
        {
            "styles": ["风格名1", "风格名2(可选)"],
            "prompt_block": "注入到system prompt的风格指令文本",
        }
    """
    # 10% 概率触发组合技
    if use_combo or random.random() < 0.10:
        combo = random.choice(COMBO_STYLES)
        style1, style2, combo_name = combo
        layer1 = STYLE_LAYERS.get(style1, {})
        layer2 = STYLE_LAYERS.get(style2, {})
        ex1 = random.choice(layer1.get("examples", [""])) if layer1 else ""
        ex2 = random.choice(layer2.get("examples", [""])) if layer2 else ""

        block = (
            f"<style-combo name=\"{combo_name}\">\n"
            f"本次回复融合两种风格：{style1} + {style2}\n"
            f"风格1示范：{ex1}\n"
            f"风格2示范：{ex2}\n"
            f"自然融合这两种风格，不要生硬拼接。\n"
            f"</style-combo>"
        )
        return {"styles": [style1, style2], "prompt_block": block}

    # 话题匹配
    topic_map = {
        "技术": ["五虎上将梗", "打工人体", "互联网黑话"],
        "bug": ["AI自嘲", "发疯文学", "打工人体"],
        "比赛": ["电竞语感", "易经锚点"],
        "感情": ["童锦程渣男语感", "AI自嘲"],
        "MBTI": ["MBTI梗"],
        "卦": ["易经锚点"],
        "累": ["佛系躺平", "打工人体"],
        "卷": ["打工人体", "佛系躺平"],
    }

    matched_pools = []
    for keyword, styles in topic_map.items():
        if keyword in message:
            matched_pools.extend(styles)

    if matched_pools:
        style_name = random.choice(matched_pools)
    else:
        # 从高频池随机
        high_freq = [name for name, layer in STYLE_LAYERS.items()
                     if layer["freq"] == "high"]
        style_name = random.choice(high_freq)

    layer = STYLE_LAYERS[style_name]
    example = random.choice(layer["examples"])

    block = (
        f"<style-hint name=\"{style_name}\">\n"
        f"本次回复参考这种风格：{layer['desc']}\n"
        f"示范：{example}\n"
        f"自然融入你的军师人设，不要机械模仿。\n"
        f"</style-hint>"
    )
    return {"styles": [style_name], "prompt_block": block}


def pick_signature() -> str:
    """随机挑一条签名。10%概率附加签名。"""
    if random.random() < 0.10:
        return "\n" + random.choice(SIGNATURES)
    return ""

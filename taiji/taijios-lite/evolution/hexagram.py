# 否定前缀：这些词紧跟关键词时反转阴阳
_NEGATIONS = ("不", "没", "别", "无", "非", "未", "莫", "甭")


def _keyword_hits(text: str, keywords: list) -> int:
    """统计关键词命中数，遇到否定前缀则不计入（留给对面计）"""
    count = 0
    for kw in keywords:
        start = 0
        while True:
            pos = text.find(kw, start)
            if pos < 0:
                break
            # 检查前1-2个字符是否为否定词
            prefix = text[max(0, pos - 2):pos]
            if any(prefix.endswith(neg) for neg in _NEGATIONS):
                pass  # 否定命中，不计入本侧
            else:
                count += 1
            start = pos + len(kw)
    return count


def _negated_hits(text: str, keywords: list) -> int:
    """统计被否定的关键词命中数（"不烦"→阴词被否定→算阳）"""
    count = 0
    for kw in keywords:
        start = 0
        while True:
            pos = text.find(kw, start)
            if pos < 0:
                break
            prefix = text[max(0, pos - 2):pos]
            if any(prefix.endswith(neg) for neg in _NEGATIONS):
                count += 1
            start = pos + len(kw)
    return count


class HexagramEngine:
    """对话状态 → 卦象 → 军师策略"""

    DECAY_ROUNDS = 5  # 超过5轮没命中的爻回归阳（中性）

    def __init__(self, data_dir: str):
        self.data_dir = data_dir
        self.state_path = os.path.join(data_dir, "hexagram_state.json")
        self.history_path = os.path.join(data_dir, "hexagram_history.jsonl")
        self.current_lines = [1, 1, 1, 1, 1, 1]  # 默认全阳（乾）
        self.current_hexagram = "乾"
        self.line_ages = [0, 0, 0, 0, 0, 0]  # 每爻距上次被命中的轮数
        self._hex_persist_count = 0  # 当前卦连续出现轮数
        self._load_state()

    # 时间加权：最新一条信号应有压倒性权重
    # 用户说"我决定了"就是在推翻之前的"犹豫"，新消息必须压过旧消息
    # 倒序：最新→最旧（最新×4, 前一条×2, 再往前×0.5）
    MSG_WEIGHTS = [5.0, 2.0, 0.5, 0.5, 0.5]

    @staticmethod
    def _weighted_line_score(messages: list, weights: list,
                             yin_kws: list, yang_kws: list):
        """按时间加权计算单爻的阴阳得分"""
        yin_score = 0.0
        yang_score = 0.0
        for i, msg in enumerate(reversed(messages)):
            w = weights[i] if i < len(weights) else 0.5
            yin_score += (_keyword_hits(msg, yin_kws) + _negated_hits(msg, yang_kws)) * w
            yang_score += (_keyword_hits(msg, yang_kws) + _negated_hits(msg, yin_kws)) * w
        return yin_score, yang_score

    def update_from_conversation(self, user_messages: list,
                                  positive_rate: float = 0.5) -> dict:
        """
        从最近对话内容更新六爻状态。
        返回当前卦象和策略。
        """
        recent = user_messages[-len(self.MSG_WEIGHTS):] if user_messages else []

        # 计算每一爻（时间加权 + 否定词处理 + 衰减机制）
        for line_num in range(1, 7):
            idx = line_num - 1
            if line_num == 6:
                # 上爻由满意率决定
                self.current_lines[5] = 1 if positive_rate >= 0.5 else 0
                self.line_ages[5] = 0
            else:
                yin_score, yang_score = self._weighted_line_score(
                    recent, self.MSG_WEIGHTS,
                    LINE_KEYWORDS[line_num]["yin"],
                    LINE_KEYWORDS[line_num]["yang"])

                if yin_score > yang_score:
                    self.current_lines[idx] = 0
                    self.line_ages[idx] = 0  # 命中，重置年龄
                elif yang_score > yin_score:
                    self.current_lines[idx] = 1
                    self.line_ages[idx] = 0
                else:
                    # 没命中：年龄+1，超过阈值回归阳（中性）
                    self.line_ages[idx] += 1
                    if self.line_ages[idx] >= self.DECAY_ROUNDS:
                        self.current_lines[idx] = 1
                        self.line_ages[idx] = 0

        # 映射到卦象
        lower = tuple(self.current_lines[0:3])
        upper = tuple(self.current_lines[3:6])
        lower_name = TRIGRAMS.get(lower, "坤")
        upper_name = TRIGRAMS.get(upper, "乾")
        self.current_hexagram = self._map_to_strategy_hexagram(
            lower_name, upper_name)

        self._save_state()
        self._log_history()

        strategy = HEXAGRAM_STRATEGIES.get(self.current_hexagram,
                                            HEXAGRAM_STRATEGIES["坤"])
        return {
            "hexagram": self.current_hexagram,
            "name": strategy["name"],
            "strategy": strategy["strategy"],
            "style": strategy["style"],
            "lines": self.current_lines.copy(),
        }

    def get_strategy_prompt(self) -> str:
        """生成注入system prompt的卦象策略"""
        strategy = HEXAGRAM_STRATEGIES.get(self.current_hexagram,
                                            HEXAGRAM_STRATEGIES["坤"])
        lines_display = "".join("⚊" if l == 1 else "⚋" for l in self.current_lines)
        return (
            f"\n## 当前卦象：{strategy['name']}（{lines_display}）\n"
            f"军师策略：{strategy['strategy']}\n"
            f"风格定位：{strategy['style']}\n"
        )

    # ── 变爻推演系统 ──────────────────────────────────────────

    def divine(self, user_messages: list, positive_rate: float = 0.5) -> dict:
        """
        易经推演：根据当前卦象 + 变爻 → 推算变卦 → 给出预判。
        在用户对话3轮以上后触发。

        返回: {
            "current": 当前卦信息,
            "changing_lines": 哪些爻在动,
            "future": 变卦信息,
            "prediction": 推演预判文本,
            "advice": 行动建议,
        }
        """
        old_lines = self.current_lines.copy()
        old_hex = self.current_hexagram

        # 先用最新消息更新卦象
        self.update_from_conversation(user_messages, positive_rate)
        new_lines = self.current_lines.copy()

        # 跟踪同卦持续轮数
        if self.current_hexagram == old_hex:
            self._hex_persist_count += 1
        else:
            self._hex_persist_count = 1

        # 检测变爻（哪些爻从上次发生了变化）
        changing = []
        for i in range(6):
            if old_lines[i] != new_lines[i]:
                changing.append(i)

        # 如果没有自然变爻，用消息情感强度推算动爻
        if not changing:
            changing = self._detect_dynamic_lines(user_messages)

        # 计算变卦（变爻取反）
        future_lines = new_lines.copy()
        for i in changing:
            future_lines[i] = 1 - future_lines[i]

        # 映射变卦
        lower = tuple(future_lines[0:3])
        upper = tuple(future_lines[3:6])
        lower_name = TRIGRAMS.get(lower, "坤")
        upper_name = TRIGRAMS.get(upper, "乾")

        # 临时切换lines算变卦名
        saved = self.current_lines
        self.current_lines = future_lines
        future_hex = self._map_to_strategy_hexagram(lower_name, upper_name)
        self.current_lines = saved

        current_strat = HEXAGRAM_STRATEGIES.get(self.current_hexagram,
                                                 HEXAGRAM_STRATEGIES["坤"])
        future_strat = HEXAGRAM_STRATEGIES.get(future_hex,
                                                HEXAGRAM_STRATEGIES["坤"])

        # 生成推演
        prediction = self._generate_prediction(
            self.current_hexagram, current_strat,
            future_hex, future_strat,
            changing, new_lines, future_lines)

        return {
            "current": {
                "hexagram": self.current_hexagram,
                "name": current_strat["name"],
                "lines": new_lines,
                "lines_display": "".join("⚊" if l == 1 else "⚋" for l in new_lines),
                "strategy": current_strat["strategy"],
            },
            "changing_lines": changing,
            "future": {
                "hexagram": future_hex,
                "name": future_strat["name"],
                "lines": future_lines,
                "lines_display": "".join("⚊" if l == 1 else "⚋" for l in future_lines),
                "strategy": future_strat["strategy"],
            },
            "prediction": prediction["prediction"],
            "advice": prediction["advice"],
            "display": prediction["display"],
        }

    def _detect_dynamic_lines(self, user_messages: list) -> list:
        """
        当没有自然变爻时，用消息内容的情感强度推算动爻。
        情感最激烈的维度 = 动爻（将要发生变化的维度）。
        """
        recent = user_messages[-len(self.MSG_WEIGHTS):] if user_messages else []
        scores = {}

        for line_num in range(1, 6):  # 1-5爻，6爻由正面率决定
            yin_score, yang_score = self._weighted_line_score(
                recent, self.MSG_WEIGHTS,
                LINE_KEYWORDS[line_num]["yin"],
                LINE_KEYWORDS[line_num]["yang"])
            # 冲突越大 = 越可能是动爻
            scores[line_num - 1] = abs(yin_score - yang_score) + (yin_score + yang_score) * 0.3

        if not any(scores.values()):
            # 完全没命中，默认五爻动（方向将变）
            return [4]

        # 取冲突最大的1-2个爻为动爻
        sorted_lines = sorted(scores.items(), key=lambda x: x[1], reverse=True)
        result = []
        for idx, score in sorted_lines:
            if score > 0:
                result.append(idx)
            if len(result) >= 2:
                break

        return result if result else [4]


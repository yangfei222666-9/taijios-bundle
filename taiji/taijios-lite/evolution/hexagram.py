    def _generate_prediction(self, current_hex, current_strat,
                              future_hex, future_strat,
                              changing, current_lines, future_lines) -> dict:
        """生成推演文本（压缩到50字以内）"""
        # 查找精确匹配的推演（支持动爻分支）
        transition = self.TRANSITION_INSIGHTS.get((current_hex, future_hex))

        if transition and isinstance(transition, dict):
            # 动爻分支：按第一个动爻选文案，fallback 到 "default"
            key = changing[0] if changing else "default"
            entry = transition.get(key, transition.get("default"))
            if entry:
                title, prediction, advice = entry
            else:
                transition = None  # fallback 到通用推演
        elif transition:
            title, prediction, advice = transition
        else:
            # 通用推演：根据阴阳变化趋势生成
            yang_now = sum(current_lines)
            yang_future = sum(future_lines)

            if yang_future > yang_now:
                title = "势起"
                prediction = f"{current_strat['name']}→{future_strat['name']}，状态在变好，抓住势头。"
                advice = future_strat['strategy'][:20]
            elif yang_future < yang_now:
                title = "蓄力"
                prediction = f"{current_strat['name']}→{future_strat['name']}，走弱，该收一收了。"
                advice = future_strat['strategy'][:20]
            else:
                title = "变局"
                prediction = f"{current_strat['name']}→{future_strat['name']}，格局在变，顺势调整。"
                advice = future_strat['strategy'][:20]

        # 变爻描述（只取第一个动爻的维度名）
        dim_name = ""
        if changing:
            _, dim_name = self.LINE_NAMES.get(changing[0], ("", ""))

        # 持续性提示：同卦连续3轮以上时补充语境
        persist = self._hex_persist_count
        persist_hint = ""
        if persist >= 5:
            persist_hint = f"｜势稳{persist}轮，格局已定"
        elif persist >= 3:
            persist_hint = f"｜连{persist}轮，趋势明确"

        # 压缩展示：一行卦变 + 一句话
        short_pred = prediction[:30] if len(prediction) > 30 else prediction
        short_advice = advice[:20] if len(advice) > 20 else advice
        display = f"[{title}] {current_strat['name']}→{future_strat['name']}"
        if dim_name:
            display += f"（{dim_name}动）"
        display += f" {short_advice}"
        if persist_hint:
            display += persist_hint

        return {
            "prediction": prediction,
            "advice": advice,
            "display": display,
        }

